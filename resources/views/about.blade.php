<section style="padding: 3%;">
  <h2 style=" color: #fd645b; font-size:xx-large; font-weight: bold;">Ease Neurocare Foundation Trust</h2>   
     <h6 style="color: #fd645b; font-size:large; font-weight: bold">Supporting Families, Empowering Carers, Enriching Lives </h6>
     <p>At Ease Neurocare Foundation Trust, based in Lagos, Nigeria, we are 
				passionately committed to supporting and empowering family carers and parents of children living with autism, learning disabilities, and associated syndromes. We believe every family deserves compassion, knowledge, and practical support on their unique journey.
      </p>
     <h2 style="color: #fd645b; font-size:xx-large; font-weight: bold">Our Mission</h2>
     <p>Our mission is to be a beacon of hope and a source of strength for families navigating the complexities  of neurodevelopmental conditions. We provide holistic care and support services that empower carers to confidently advocate for their children's needs and foster an environment where every child can thrive.
     </p>

     <h2 style="color: #fd645b; font-size:xx-large; font-weight: bold">What we offer</h2>
     <p>We understand that caring for a child with special needs requires more than just love it requires expert support and tailored services. That's why we offer:
      <ul>
        <li>Support groups to connect you with others who truly understand your journey </li>
        <li> Training sessions that equip carers with valuable skills and knowledge Comprehensive assessments to understand each child's unique needs </li>
        <li>Personalized care planning and regular care reviews to adapt as your child grows </li>
        <li>Occupational therapy, physiotherapy, and therapeutic support both at home and in school settings</li>
      </ul> 
      <h2 style="color: #fd645b; font-size:xx-large; font-weight: bold">Who we support</h2>
      <p>Our services are designed for family carers and parents of children of all ages living with autism, learning disabilities, and related conditions. Whether you are just beginning to understand your child's needs or have been caring for years, Ease Neurocare is here to walk beside you every step of the way.
      </p>
      <h2 style="color: #fd645b; font-size:xx-large; font-weight: bold">Our values</h2>
      <p>
        <ul>
          <li>Compassion - We listen deeply and respond with empathy and kindness.</li>
          <li>Empowerment - We provide tools and knowledge that help carers become confident advocates.</li>
          <li>Inclusion - We champion accessibility, respect, and understanding in all areas of life.</li>
          <li>Community - We foster connections that bring strength and hope through shared experience.</li>
        </ul>
      </p>
      <h2 style="color: #fd645b; font-size:xx-large; font-weight: bold">Join our community</h2>
      <p>
        At Ease Neurocare Foundation Trust, you are never alone. Join us to access vital resources, compassionate support, and a community that truly cares. Together, we can build brighter futures for our children and families.
      </p>    
      </p>
     <p><a href="workwithus.html" class="btn btn-secondary">Work with Us</a></p>
</section>

